#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

int main()
{
	int *ptr2;
	int *ptr;
	int a;
	int b;

	a = 56;
	b = 89;
	ptr = &a;
	ptr2 = &b;
	ft_swap(ptr, ptr2);
	printf("%d , %d", a, b);
}
