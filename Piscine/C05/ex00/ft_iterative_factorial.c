int	ft_iterative_factorial(int nb)
{
	int	fact;
	int	i;
	
	i = 1;
	fact = 1;
	if (nb < 0 || nb >= 13)
		return (0);
	while (i < nb)
	{
		fact = fact * i;
		i++;
	}
	return (fact);
}
