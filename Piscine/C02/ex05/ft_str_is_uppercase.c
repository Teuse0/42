#include <stdio.h>

int	ft_str_is_uppercase(char *str)
{
	int		i;

	i = 0;
	while (str[i] >= 'A' && str[i] <= 'Z')
	{
		i++;
	}
	if (str[i] == '\0')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}

int	main(void)
{
	char	*str;
	int	i;

	str = "cONNARDDEVIRUS";
	i = ft_str_is_uppercase(str);
	printf("%d", i);
}
