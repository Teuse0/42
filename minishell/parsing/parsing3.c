#include "../minishell.h"

int	ft_size_tab(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] == '|' || str[i] == '<' || str[i] == '>')
		{
			if ((str[i] == '>' && str[i + 1] == '>')
				|| (str[i] == '<' && str[i + 1] == '<'))
				i++;
			j++;
		}
		i++;
	}
	if (j == 0)
		return (1);
	j = j * 2 + 1;
	return (j);
}
